#get-date

Write-Host (Get-Date).AddDays(1) #adiciona um dia a data atual
Write-Host (Get-Date).AddDays(-1) #subtrai um dia da data atual
Write-Host (Get-Date).AddHours(1) #adiciona uma hora a data atual
Write-Host (Get-Date).AddHours(-1) #subtrai uma hora da data atual
Write-Host (Get-Date).AddMinutes(30) #adiciona 30 minutos a data atual
Write-Host (Get-Date).AddMinutes(-30) #subtrai 30 minutos
Write-Host (Get-Date).AddSeconds(10) #adiciona 10 segundos a data atual
Write-Host (Get-Date).AddSeconds(-10) #subtrai 10 segundos
Write-Host (Get-Date).AddMonths(1) #adiciona um mês a data atual
Write-Host (Get-Date).AddMonths(-1) #subtrai um mês da data atual
Write-Host (Get-Date).AddYears(1) #adiciona um ano a data atual
Write-Host (Get-Date).AddYears(-1) #subtrai um ano da data atual